C   08/10/82 305222232  MEMBER NAME  TRAJEC   (S)           FORTRAN
      SUBROUTINE TRAJEC
C
C     ROUTINE TO COMPUTE TRADJECTORIES FROM TWISS PARAMETERS
C
C
#include "cbeta.for"
      DIMENSION XP(10),X(10)
C
      WRITE(6,1001)
 1001 FORMAT('1',' NUM MAG       POS    BETA  ALPHA PHASE TRANVERSE MOTI
     +ON---------------->')
C1001 FORMAT('1',' NUM MAG       POS    BETA  ALPHA PHASE TRANVERSE POSI
C    +TION-------------->')
      IF(IT.GT.498)IT=498
      DO 10 J=1,IT
      DO 11 L=1,8
      PHAS= PHASX(J)+2.*3.14159*0.125*(L-1)
      XP(L)=1./SQRT(BETX(J))*(-ALPHX(J)*SIN(PHAS)+COS(PHAS))
      X(L)=SQRT(BETX(J))*SIN(PHAS)
   11 CONTINUE
      WRITE(6,1000)J,MAG(J),SX(J),BETX(J),ALPHX(J),PHASX(J),
     X XP
 1000 FORMAT(' ',I4,1X,A4,1X,F9.2,2F6.1,F7.2,10F8.2)
   10 CONTINUE
      WRITE(6,1002)
 1002 FORMAT(' ',///)
      DO 100 J=1,IT
      SX(J)=-SX(J)
      DO 110 L=1,8
      PHAS=-PHASX(J)+2.*3.14159*0.125*(L-1)
      XP(L)=1./SQRT(BETX(J))*(ALPHX(J)*SIN(PHAS)+COS(PHAS))
      X(L)=SQRT(BETX(J))*SIN(PHAS)
  110 CONTINUE
      WRITE(6,1000)J,MAG(J),SX(J),BETX(J),ALPHX(J),PHASX(J),
     X XP
  100 CONTINUE
      RETURN
      END
